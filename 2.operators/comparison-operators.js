
// The result of a comaprison operator is a boolean, a true or false statement

// Relational Operators:
// > greater than
// < less than
// >= greater than or equal to
// <=less than or equal to

let x = 1;

console.log(x > 0);
console.log(x >= 1);
console.log(x < 0);

//Equality Operators
// === equal to
// !== not equal to

console.log(x === 1);
console.log(x === 2);
console.log(x !== 2);
console.log(x !== 1);

// the last statement is false because x is equal to 1