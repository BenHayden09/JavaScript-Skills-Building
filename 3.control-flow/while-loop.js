// For loop = loop variable is part of the loop itself
// While loops = you have to declare 'let' the varaible separately 

for (let i = 0; i <=5; i++) {
    if (i % 2 !==0) console.log(i);
}

let i = 0
while (i <=5) {
    if (i % 2 !==0) console.log(i);
    i++;
}

// For loop cycles through linear
// While loop cycles in stack/vertically